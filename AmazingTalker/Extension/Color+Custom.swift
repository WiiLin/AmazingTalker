import UIKit

extension UIColor {
    static var disableGray: UIColor? {
        return UIColor(named: "Disable Gray")
    }

    static var enableGreen: UIColor? {
        return UIColor(named: "Enable Green")
    }

    static var textBlack: UIColor? {
        return UIColor(named: "Text Black")
    }
}
